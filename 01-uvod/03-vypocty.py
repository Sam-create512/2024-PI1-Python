a = float(input("Zadaj číslo a: ")) # Input je príkazom vstupu. Vypíše nám na obrazovku text v zátvorke a to, čo užívateľ napíše na klávesnici, uloží do premennej a
b = float(input("Zadaj číslo b: ")) # Input vracia vždy výsledok vo formáte reťazca (postupnosť znakov resp. text), funkcia int() pretypuje reťazecna celé číslo
sucet = a + b
print("Súčet je: ", sucet)
rozdiel = a - b
print("Rozdiel je: ", rozdiel)
sucin = a * b
print("Súčin je: ", sucin)
podiel = a / b
print("Podiel je ", podiel)
